// string
let firstName = "Pravin";
console.log(firstName, typeof firstName);

// numbers
let currentAge = 24;
console.log(currentAge, typeof currentAge);

// boolean
let booleanTrue = true;
let booleanFalse = false;
console.log(booleanFalse, booleanTrue, typeof booleanTrue);

// undefined
let varUndefined;
console.log(varUndefined, typeof varUndefined);

// null
let varNull = null;
console.log(varNull, typeof varNull);

// symbol
let symbol = Symbol("INR");
console.log(symbol, typeof symbol);

// bigint
let bigInt = 8n;
console.log(bigInt, typeof bigInt);
